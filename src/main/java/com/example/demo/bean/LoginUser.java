package com.example.demo.bean;

public class LoginUser {
    private String testCustFlag;
    private String newCustFlag;
    private String userId;
    private String token;

    public String getTestCustFlag() {
        return testCustFlag;
    }

    public void setTestCustFlag(String testCustFlag) {
        this.testCustFlag = testCustFlag;
    }

    public String getNewCustFlag() {
        return newCustFlag;
    }

    public void setNewCustFlag(String newCustFlag) {
        this.newCustFlag = newCustFlag;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
